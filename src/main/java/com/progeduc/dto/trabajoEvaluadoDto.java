package com.progeduc.dto;

public class trabajoEvaluadoDto {
	
	Integer id;
	String ods;
	String categoria;
	String nivel_participacion;
	String codigoie;
	String nombreie;
	String codigo_trabajo;
	String modalidad;
	String titulo;
	Integer evaluadores_asignados;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getOds() {
		return ods;
	}
	public void setOds(String ods) {
		this.ods = ods;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getNivel_participacion() {
		return nivel_participacion;
	}
	public void setNivel_participacion(String nivel_participacion) {
		this.nivel_participacion = nivel_participacion;
	}
	public String getCodigoie() {
		return codigoie;
	}
	public void setCodigoie(String codigoie) {
		this.codigoie = codigoie;
	}
	public String getNombreie() {
		return nombreie;
	}
	public void setNombreie(String nombreie) {
		this.nombreie = nombreie;
	}
	public String getCodigo_trabajo() {
		return codigo_trabajo;
	}
	public void setCodigo_trabajo(String codigo_trabajo) {
		this.codigo_trabajo = codigo_trabajo;
	}
	public String getModalidad() {
		return modalidad;
	}
	public void setModalidad(String modalidad) {
		this.modalidad = modalidad;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public Integer getEvaluadores_asignados() {
		return evaluadores_asignados;
	}
	public void setEvaluadores_asignados(Integer evaluadores_asignados) {
		this.evaluadores_asignados = evaluadores_asignados;
	}	
}
