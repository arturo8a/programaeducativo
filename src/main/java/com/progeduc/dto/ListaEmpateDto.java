package com.progeduc.dto;

public class ListaEmpateDto {
	

	Integer anio;
	Integer idOds;
	Integer idCategoria;
	String idNivelParticipacion;
	Integer puesto;
	
	public Integer getAnio() {
		return anio;
	}
	public void setAnio(Integer anio) {
		this.anio = anio;
	}
	public Integer getIdOds() {
		return idOds;
	}
	public void setIdOds(Integer idOds) {
		this.idOds = idOds;
	}
	public Integer getIdCategoria() {
		return idCategoria;
	}
	public void setIdCategoria(Integer idCategoria) {
		this.idCategoria = idCategoria;
	}
	public String getIdNivelParticipacion() {
		return idNivelParticipacion;
	}
	public void setIdNivelParticipacion(String idNivelParticipacion) {
		this.idNivelParticipacion = idNivelParticipacion;
	}
	public Integer getPuesto() {
		return puesto;
	}
	public void setPuesto(Integer puesto) {
		this.puesto = puesto;
	}
}
