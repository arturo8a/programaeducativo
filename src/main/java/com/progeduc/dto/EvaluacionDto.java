package com.progeduc.dto;

public class EvaluacionDto {
	
	Integer id;
	Integer anio;
	String nivelparticipacion;
	String categoriaevaluacion;
	String estado;
	Integer estadoevaluacion;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getAnio() {
		return anio;
	}
	public void setAnio(Integer anio) {
		this.anio = anio;
	}
	public String getNivelparticipacion() {
		return nivelparticipacion;
	}
	public void setNivelparticipacion(String nivelparticipacion) {
		this.nivelparticipacion = nivelparticipacion;
	}
	public String getCategoriaevaluacion() {
		return categoriaevaluacion;
	}
	public void setCategoriaevaluacion(String categoriaevaluacion) {
		this.categoriaevaluacion = categoriaevaluacion;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public Integer getEstadoevaluacion() {
		return estadoevaluacion;
	}
	public void setEstadoevaluacion(Integer estadoevaluacion) {
		this.estadoevaluacion = estadoevaluacion;
	}

}
